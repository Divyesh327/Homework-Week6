package HomeworkWeek6;

public class Program15
{

    public static void main(String[] args)
    {

        int N1 = 100, N2 = 200;

        System.out.println("Before Swap");
        System.out.println("N1 = " + N1);
        System.out.println("N2 = " + N2);

        int temp = N1;
        N1 = N2;
        N2 = temp;

        System.out.println("After swap");
        System.out.println("N1 = " + N1);
        System.out.println("N2 = " + N2);
    }




}
